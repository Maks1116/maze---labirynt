function setLevel (lvl: number) {
    if (lvl == 0) {
        tiles.setTilemap(tilemap`level1`)
    } else if (lvl == 1) {
        tiles.setTilemap(tilemap`level2`)
        create_wroga()
        create_wroga()
        create_ciastko()
        create_ciastko()
    } else if (lvl == 2) {
        tiles.setTilemap(tilemap`level0`)
        create_wroga()
        create_wroga()
        create_ciastko()
        create_ciastko()
    } else {
        game.over(true)
    }
}
